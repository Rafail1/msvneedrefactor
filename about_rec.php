                <p>Вам не нужно смотреть на часы во время пребывания на нашей студии, так как в оплату входит только время проведенное за работой. У нас Вы можите спокойно и не торопясь настроиться, распеться попить чайку, делать перерывы во время записи которые так же не входят в оплату. Вас проконсультируют по любым вопросам связанным со звукозаписью. </p>
                <br>
                <h2>Процесс записи на MSV</h2>
                <p><h3>1. Начало записи. Разминка</h3>

Не забудьте взять с собой текст песни (если плохо помните слова) и музыку на flash-карте, либо на CD.
После того как звукорежиссер настроит аппаратуру, он попросит вас надеть наушники, подойти к микрофону  и пропеть пару раз всю песню от начала до конца. В наушниках зазвучит ваша музыка и вы услышите свой голос. Убедитесь, что вы отчетливо слышите свой вокал и музыку в наушниках. Если вам плохо слышно ваш голос или минусовку, не стесняясь, сообщите об этом звукорежиссеру и он отстроит баланс громкости на ваш вкус. Такая пробная запись делается для того, чтобы Вы смогли себя комфортно чувствовать во время записи.</p>
<p><h3>2. Запись песни</h3>
После того, как все подготовлено, приступают непосредственно к записи. 
Всегда помните о том, что звукорежиссер опытный специалист. В зависимости от сложности и специфики музыкальной композиции, а также ваших вокальных данных, он подберет оптимальную схему записи вашего вокала. Возможно вам предложат записать песню целиком, по куплетам, по припевам, по нескольким строчкам и т.д. В процессе работы над песней сразу будут выявлены "проблемные места", на которые нужно будет обратить особое внимание.
Чувство страха ошибиться, не попасть в ноту, забыть слова и прочие психологические барьеры - пожалуй самые главные факторы, которые мешают начинающим вокалистам, сосредоточиться на исполнении песни.
В такой ситуации вам необходимо уяснить для себя, что любая ваша ошибка при исполнении композиции может быть исправлена. Очень часто записывают много дублей, вырезают из них лучшие моменты и компонуют один самый лучший. Поэтому места, в которых были допущены огрехи, удалят и заменят на более удачные.
</p>
<p>Отлично записать песню с первого или со второго дубля под силу только профессиональным певцам, имеющим многолетний опыт концертной и студийной работы. Поэтому бояться тут нечего. Следуйте советам и рекомендациям звукорежиссера и все обязательно получится!</p>

              <h2>Как мы работаем</h2>
              <p class="by">Подготовил: <span>Михаил Стотик</span></p>
              <p class="posted"><span><i class="fa fa-clock-o"></i> Размещено</span> 2-го Марта</p>
              <div class="regular-text">
                <p>Над сведением звука у нас работает два звукоинженера, так как одна из задач микширования это добиться обьективного звучания, что  практически не возможно сделать одному человеку из-за физиологии нашего организма. Так же это позволяет более обьективно, эфективно и четко решать поставленные задачи.</p>
                <br>
                <h2>Процесс сведения на MSV</h2> 
               <h3>1. Подготовка</h3>
                <p>Мы общаемся с заказчиком, с целью узнать чего он ожидает от сведения композиции. Если заказчику сложно объяснить профессиональным языком чего он хочет, мы предлагаем подобрать для нас несколько уже изданых треков похожих по звучанию.</p>
                <h3>2. Видение и План</h3>
                <p>Далее идет составление плана по сведению, обрисовка общей картины. Мы выясняем и расписываем сколько планов будет в композииции, где и какие элементы будут размещаться в этих планах. Какие эфекты будут использоваться во время сведения, как мы можем подчеркнуть или выразить отдельные элементы аранжировки и тд.</p>
               <h3>3. Подготовка и настройка проекта</h3>
                <p>После загрузки мультитрека в секвенсор необходимо настроить проект, для удобной и быстрой работы. Поскольку мы подходим к каждому проекту индивидуально, то о шаблонах и речи быть не может, по этому иногда настройка проекта занимает не малое колличество времени. Необходимо расставить все дорожки по группам и в порядке сведения, далее настроить группы и отдельные каналы. После настройки дорожек мы переходим к настройке шин с эфектами, на которые будем посылать сиглал с аудиодорожек во время сведения. Иногда приходиться перебрать не один десяток эффектов, что бы грамотно настроить возвраты.</p>
                <h3>4. Сведение</h3>
                <p>И вот мы наконец подошли к сведению! Для началы мы внимательно прослушиваем каждую дорожку и удаляем шумы и щелчки если такие имеются. Далее выстраиваем баланс и панораму. Работаем с динамикой и устраняем частотные конфликты микса. Выстраиваем планы и пространство композиции. Как правило это самый сложный, самы долгий, но самый увлекательный этап в сведении.</p>
                
                <p> PS: На MSV мы сводим композицию в -3db пиковой громкости, для того что бы не было перегрузов и искажений. Если Вы будите отправлять свой трек на другую студию для мастеринга, Вас в большинстве случаев попросят привести его в данный вид!</p>

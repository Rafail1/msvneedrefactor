              <div class="regular-text">
                <p>В наше время мастеринг это больше технический процесс, который требует знаний и пониманий физики звука, акустики, софта и железа для обработки. Обладая всеми вышеперечисленными навыками, наши звукоинженеры справляются с любой поставленной задачей. Вы получаете громкий, конкурентноспособный, частотно и фазово сбалансированный, материал готовый к тиражированию.</p>
                <br>
                <h2>Процесс мастеринга на MSV</h2>
                <h3>1. Анализ фонограммы и оценка ее качества</h3> 
                <p>На этом этапе выявляются разного рода дефекты: шумы, щелчки, треск, импульсные помехи, наводки и искажения сигнала. В заключение мастера в студии решают, нужно ли делать мастеринг трека. Если на фонограмме присутствуют неисправимые дефекты, то ее обработка бессмысленна.</p>
                
<h3>2. Монтаж.</h3> 
<p>Осуществляется в том случае, если на фонограмме нет технических дефектов. Необходим в ситуациях, когда имеются лишние паузы и требуется обрезать начало и конец.</p>

<h3>3. Изменение уровня сигнала</h3> 
<p>На этом этапе происходит нормализация сигнала до заданного уровня. Повышается или понижается пиковый уровень фонограммы.</p>

<h3>4. Устранение помех путем фильтрации</h3> 
<p>Во многих фонограммах присутствуют звуки, которые выходят за рамки тех, что воспринимаются человеческим слухом. Чаще всего их можно услышать на низких частотах. Фильтрация убирает эти звуки и удаляет частоты выше 18-20 kHz и ниже 20-40 Hz.</p>

<h3>5. Эквализация</h3> 
<p>Этот процесс бывает двух типов — художественный и технический. Первый задействуется в случаях, когда звучание нужно сделать прозрачным или более насыщенным. Техническая эквализация нормализует тональный баланс.</p>

<h3>6. Компрессия</h3> 
<p>Этот этап немаловажен, так как во время него сужается динамический диапазон, а фонограмме придается необходимая плотность. В результате этого увеличивается воспринимаемая громкость.</p>

<h3>7. Усиление стереоэффекта</h3> 
<p>Путем расширения стереобазы происходит трансформация звука: он приобретает большой объем и становится значительно шире. Однако чем объемнее композиция, тем ниже ее плотность.</p>

<h3>8. Обработка при помощи эксайтера</h3> 
<p>В процессе данного этапа в композицию добавляются новые гармоники, за счет чего высокие частоты усиливаются и звук становится очень четким. Однако эксайтерная обработка подходит далеко не для всех фонограмм, так как может сделать звук чересчур резким.</p>

<h3>9. Доведение звучания до максимальной громкости</h3> 
<p>Делается это с помощью максимайзера и приборов динамической обработки. Путем отстройки, нормализации и компрессии мастера добиваются высокой воспринимаемой громкости мелодии.

<h3>Мастеринг альбома</h3> 	
Если делается мастеринг альбома, то составляется последовательность аудиодорожек, создание плавного затухания звука и длительность пауз между треками.</p>

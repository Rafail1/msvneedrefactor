              <h2>Как мы работаем</h2>
              <p class="by">Подготовил: <span>Михаил Стотик</span></p>
              <p class="posted"><span><i class="fa fa-clock-o"></i> Размещено</span> 2-го Марта</p>
              <div class="regular-text">
                <p>Мы хотим делать эксклюзивную и интересную музыку, поэтому в создании аранжировки участвует вся наша команда: композиторы, музыканты и звукоинженеры. Такой подход добавляет к звучанию аранжировки больше эмоций, креатива и разнообразия.</p>
                <h2>Процесс аранжировки композиции на MSV</h2>
                <h3>1. Сбор данных</h3>
                <ui>
                <li>Мы общаемся с заказчиком, с целью узнать чего он ожидает от аранжировки своей композиции. Выясняем форму, темп, звучание аранжировки, какие инструменты будут звучать. Если заказчику сложно объяснить профессиональным языком чего он хочет, мы предлагаем подобрать для нас несколько уже изданых треков похожих по звучанию.</li>
                <li>Проводим анализ исполнителя для данной композиции. Прослушиваем исполнителя и выявляем его сильные и слабые профессиональные качества.</li>
                </ui>
                <h3>2. Рыба</h3>
                <p>Создание наброска, это как правило куплет-припев и возможно проигрыш. Заказчик утверждает или корректирует проделанную работу.</p>
                <h3>3. Доработка</h3>
                <p>Далее мы выстраиваем форму, дополняем и делаем аранжировку более насыщенной и разнообразной, если этого требует заказчик</p>
                <h3>4. Звуковой дизайн и запись живых инструментов</h3>
                <p>Следующий шаг в аранжировке это саунд(звуковой)-дизайн и запись живых инструментов(если этого требует аранжировка). Мы тщательно подбираем звуки, добиваемся от них неповторимого, интересного и уникального звучания. Если необходимо записать живые инструменты, мы приглашаем профессиональных музыкантов, с хорошо звучащим инструментом и богатым опытом игры.</p>
                <h3>5. Монтаж и подготовка к сведению</h3>
                <p>Это заключительный этап в аранжировке. После всей проделанной работы, мы монтируем и понятно подписываем все дорожки аранжировки и рендерим все звуки в аудиофайлы. Мультитрек для сведения создается в формате заранее оговоренном с заказчиком, как правило это -  WAV или AIFF 44.1 kHz / 24 bit. 
</p> <p>Если есть необходимость мы так же можем предоставить MIDI файлы аранжировки.</p>
<br>
<p>Мультитрек можно забрать у нас лично на флешке, так же мы можем выслать по электронной почте или через файлообменник.</p>
